#include "3-calc.h"

/**
 * op_add - Add two ints
 * @a: first int
 * @b: second int
 * Return: a + b;
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Returns the difference of a and b
 * @a: first int
 * @b: second int
 * Return: a - b;
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two ints
 * @a: first int
 * @b: second int
 * Return: a * b;
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide int a by int b
 * @a: first int
 * @b: second int
 * Return: a / b;
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of the div of a by b
 * @a: first int
 * @b: second int
 * Return: a % b;
 */
int op_mod(int a, int b)
{
	return (a % b);
}

